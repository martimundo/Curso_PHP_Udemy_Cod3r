<p>A aritmética (da palavra grega ἀριθμός, arithmós,[Nota 1] "número") é o ramo mais elementar e antigo da matemática, lida com as operações possíveis entre os números; é utilizada por quase todo ser humano: seja em tarefas cotidianas, seja em tarefas científicas ou negociais. As quatro operações matemáticas mais elementares (adição, subtração, multiplicação e divisão) são a aritmética elementar.

Por vezes, matemáticos profissionais utilizam o termo "aritmética superior"[1] para referirem-se àlgum resultado mais avançado relacionado à teoria dos números; contudo, da aritmética elementar, isso não é parte.</p>
